     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        SubMenuItem::create([
            'menuitem_id' => $request->menuItem,
            'name' => $request->name,
            'status' => $request->status,
            'link' => $request->link,
        ]);
        return redirect(route('submenus'))->with('success', "SubMenu Item created Successfully");
    }

